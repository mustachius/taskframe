// Wordmark returns the logo lines colored with the theme's vertical gradient
// (GradFrom‚ÜíGradTo), falling back to a flat accent when no endpoints are set.
func Wordmark(th Theme, ascii bool) []string {
	art := WordmarkShadow
	if ascii {
		art = WordmarkASCII
	}
	if th.GradFrom != "" && th.GradTo != "" {
		return GradientBlock(art, th.GradFrom, th.GradTo)
	}
	out := make([]string, len(art))
	for i, line := range art {
		out[i] = th.Accent.Render(line)
	}
	return out
}
